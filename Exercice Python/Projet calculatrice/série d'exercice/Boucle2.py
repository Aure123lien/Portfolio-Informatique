# while : Tant que la condition est vraie
#salarié 2000 euros par mois
salaire = 2000

# tant que le salaire sera inférieur à 2500 euros, il recevra 200 euros en plus de son salaire
while salaire < 2500:
    # ajout de 200 euros au salaire actuel
    salaire = salaire + 200
    # Cela va afficher le résultat jusqu'a dépasser 2500 euros
    print("Le salaire est maintenant de :", salaire)